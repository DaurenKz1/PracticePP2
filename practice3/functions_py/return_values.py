def add(a, b):
    return a + b

result = add(3, 5)
print(result)   

def square(x):
    return x * x

print(square(4))   

def is_even(n):
    return n % 2 == 0

print(is_even(6))   
print(is_even(7))   

def greet(name):
    return "Hello, " + name

print(greet("Dauren"))

def maximum(a, b):
    if a > b:
        return a
    else:
        return b

print(maximum(10, 7))  


